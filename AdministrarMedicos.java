    public void mostrarDatosMedicos(){
        
        DefaultTableModel tMedicos = new DefaultTableModel();
        
        tMedicos.addColumn("CodigoTarjetaProfesional");
        tMedicos.addColumn("Especialidad");
        tMedicos.addColumn("AÃ±osExperiencia");
        tMedicos.addColumn("Consultorio");
        tMedicos.addColumn("Domicilio");
        
        tablaMedicos.setModel(tMedicos);
        
        String[] datos = new String[5];
        
        try{
            Statement leer = connect.createStatement();
            ResultSet resultado = leer.executeQuery("SELECT * FROM medicos");
            
            while(resultado.next()){
                datos[0] = resultado.getString(1);
                datos[1] = resultado.getString(2);
                datos[2] = resultado.getString(3);
                datos[3] = resultado.getString(4);
                datos[4] = resultado.getString(5);
                
                tMedicos.addRow(datos);
            }
            tablaMedicos.setModel(tMedicos);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,e, "Error en SELECT Medicos",2);
        }
        
    }
